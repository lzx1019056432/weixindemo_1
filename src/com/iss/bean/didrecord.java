	private String didnum;
	private String name;
	private String didtime;
	private String didmoney;
	private String state;
	private String content;
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
	public String getDidnum() {
		return didnum;
	}
	public void setDidnum(String didnum) {
		this.didnum = didnum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDidtime() {
		return didtime;
	}
	public void setDidtime(String didtime) {
		this.didtime = didtime;
	}
	public String getDidmoney() {
		return didmoney;
	}
	public void setDidmoney(String didmoney) {
		this.didmoney = didmoney;
	}
