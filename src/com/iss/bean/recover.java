package com.iss.bean;

public class recover {
	private String reuid;
	private String u_openid;
	private double lng;//经度
	private double lat;//纬度
	private String photonum;//电话
	private String imgurl;
	private int state;
	private String name;
	public String getReuid() {
		return reuid;
	}
	public void setReuid(String reuid) {
		this.reuid = reuid;
	}
	public String getU_openid() {
		return u_openid;
	}
	public void setU_openid(String u_openid) {
		this.u_openid = u_openid;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public String getPhotonum() {
		return photonum;
	}
	public void setPhotonum(String photonum) {
		this.photonum = photonum;
	}
	public String getImgurl() {
		return imgurl;
	}
	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
